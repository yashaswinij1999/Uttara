const mongoose = require("mongoose");

const usersSchema = new mongoose.Schema({
  fname: {
    type: String,
    required: true,
  },
  lname: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  phoneNo: {
    type: String,
    required: true,
  },
});

const Users = mongoose.model("Users", usersSchema);
module.exports = Users;
